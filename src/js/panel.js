var objct = require("objct");

///////////////////////////////////////////////////////////////////////////////

var panel = {

  toggle : toggle

};

///////////////////////////////////////////////////////////////////////////////

module.exports = objct(require("./globals"), panel);

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

function toggle(){

  var panel = this;

  if(panel.getAttribute('open') === "true") {
    panel.setAttribute("open", "false");
  }
  else {
    panel.setAttribute("open", "true");
  }

}

